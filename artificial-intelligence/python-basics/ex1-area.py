'''
1. Faça o algoritmo para calcular a área (AREA) de uma sala, sendo que os
comprimentos das paredes (L e C) são fornecidos pelo usuário. Apresente a área depois de
calculada.
'''

c = float(input("Insira o comprimento da sala: "))
l = float(input("Insira o lado da sala: "))
a = c*l

print(f"A sala tem {a} de área!")